	<p style="font-family:inherit;font-size:inherit;font-style:inherit;font-weight:inherit;vertical-align:baseline;">
		日前，北京新浪支付科技有限公司（下称“新浪支付”）与中国人民财产保险股份有限公司西安市分公司（下称“中国人保西安分公司”）举行签约仪式，双方在企业账户资金安全保险方面建立合作关系，以保障企业账户的资金安全。此次合作得到双方领导高度重视和支持，对于双方来讲都是一次创新和尝试。
	</p>
	<p style="font-family:inherit;font-size:inherit;font-style:inherit;font-weight:inherit;vertical-align:baseline;">
		根据协议，双方合作后，所有在新浪支付开设资金账户，包括微钱包、微财富、存钱罐、企业魔方、微筹集以及其他存管在新浪支付系统的账户的机构组织，都将由中国人保西安分公司提供企业账户资金安全保险。上述机构组织的企业账户在挂失或冻结前72小时内，有发生损失的情形，中国人保西安分公司将按照保险合同的约定负责赔偿。
	</p>
	<p style="font-family:inherit;font-size:inherit;font-style:inherit;font-weight:inherit;vertical-align:baseline;">
		近年来，网络支付的快速发展给企业带来便利的同时，也伴随着一些风险和麻烦，因盗刷、盗用、复制而导致企业资金遭受损失的概率越来越大，企业账户资金安全保险正是为了防范此类风险而来，它可为企业客户的账户提供全面保障，企业名下的所有第三方虚拟账户等均可保。同样保障在企业员工被歹徒胁迫的状态下，将企业账户号码及密码透露给他人导致企业的资金损失。
	</p>
	<p style="font-family:inherit;font-size:inherit;font-style:inherit;font-weight:inherit;vertical-align:baseline;">
		新浪支付相关负责人表示，中国人保西安分公司在企业账户资金安全险方面拥有丰富的经验，此次双方合作将为企业客户提供更好的保障服务，后期双方将在多个领域共同探讨合作机会。
	</p>
	<p style="font-family:inherit;font-size:inherit;font-style:inherit;font-weight:inherit;vertical-align:baseline;">
		据了解，新浪支付与WeX微汇金融共同推出的WeX资金账户管理服务，是为互联网金融平台商户提供的一整套资金账户管理系统及服务，商户平台上的用户以实名制在资金账户管理系统中开设账户，实现充值、还款、投标、放款、提现等功能。WeX资金账户管理服务将资金全程存管在新浪支付银行备付金账户，避开资金池风险，实时监控，合规透明。该服务已为400家主流互联网金融平台以及其他行业提供服务。
	</p>
